function Mostrar()
{
	alert('iteración while');

	var contador=10 //comenzara desde el 10 descendentetemente

	while (contador > 0) //Cuenta todos los numero mayores a 0

	{
		document.write("  Numero:  " + contador + "<br>" );
		contador--; //De esa manera resta
		console.log(contador);
	}



}//FIN DE LA FUNCIÓN